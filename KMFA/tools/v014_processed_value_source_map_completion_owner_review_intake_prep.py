def _now(generated_at: str | None = None) -> str:
    return generated_at or datetime.now().astimezone().isoformat(timespec="seconds")


def _read_json(path: Path) -> dict[str, Any]:
    value = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(value, dict):
        raise ValueError(f"{path} must contain a JSON object")
    return value


def _write_json(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def _write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def _git_output(args: list[str]) -> str:
    result = subprocess.run(
        ["git", "-c", "core.quotePath=false", *args],
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        check=False,
    )
    if result.returncode != 0:
        return "UNKNOWN"
    return result.stdout.strip()


def _git_check_ignored(path: Path) -> bool:
    return subprocess.run(["git", "check-ignore", "-q", path.as_posix()], check=False).returncode == 0


def _group_id(index: int, context_group: str, status: str) -> str:
    normalized = "".join(ch if ch.isalnum() else "-" for ch in context_group).strip("-").lower()
    return f"ORG-{index:03d}-{normalized}-{status[:12]}"


def _top_candidate_records(context_candidate: dict[str, Any]) -> list[dict[str, Any]]:
    records = context_candidate.get("top_candidate_records", [])
    if not isinstance(records, list):
        return []
    result: list[dict[str, Any]] = []
    seen: set[str] = set()
    for record in records:
        if not isinstance(record, dict):
            continue
        key = str(record.get("record_ref_hash") or record.get("numeric_value_fingerprint") or len(result))
        if key in seen:
            continue
        seen.add(key)
        enriched = deepcopy(record)
        enriched["candidate_rank"] = len(result) + 1
        result.append(enriched)
        if len(result) >= 20:
            break
    return result


def _build_private_review_package(
    *,
    generated_at: str,
    source_draft: dict[str, Any],
    source_diagnostic: dict[str, Any],
) -> tuple[dict[str, Any], dict[str, Any], dict[str, Any], str]:
    items = source_draft.get("candidate_completion_items", [])
    if not isinstance(items, list):
        raise ValueError("candidate_completion_items must be a list")
    context_candidates = source_draft.get("context_candidates", {})
    if not isinstance(context_candidates, dict):
        raise ValueError("context_candidates must be an object")

    by_context: dict[str, list[dict[str, Any]]] = {}
    for item in items:
        if isinstance(item, dict):
            by_context.setdefault(str(item.get("context_group", "")), []).append(item)

    review_groups: list[dict[str, Any]] = []
    response_rows: list[dict[str, Any]] = []
    candidate_catalog_records: list[dict[str, Any]] = []
    for group_index, context_group in enumerate(sorted(by_context), 1):
        group_items = by_context[context_group]
        context_candidate = context_candidates.get(context_group, {})
        if not isinstance(context_candidate, dict):
            context_candidate = {}
        status = str(context_candidate.get("candidate_status", "missing_context_candidate"))
        group_id = _group_id(group_index, context_group, status)
        top_records = _top_candidate_records(context_candidate)
        for record in top_records:
            catalog_record = deepcopy(record)
            catalog_record["review_group_id"] = group_id
            catalog_record["context_group"] = context_group
            candidate_catalog_records.append(catalog_record)
        review_groups.append(
            {
                "review_group_id": group_id,
                "context_group": context_group,
                "candidate_status": status,
                "target_slot_count": len(group_items),
                "target_slot_ids": [item.get("target_slot_id") for item in group_items],
                "candidate_record_count": int(context_candidate.get("candidate_record_count", 0) or 0),
                "candidate_unique_numeric_fingerprint_count": int(
                    context_candidate.get("candidate_unique_numeric_fingerprint_count", 0) or 0
                ),
                "top_candidate_record_count": len(top_records),
                "top_candidate_records": top_records,
                "owner_review_instruction_zh": (
                    "请为该组确认可复用候选排名、指定单条 candidate record ref，或选择 keep_pending / "
                    "mark_not_applicable / request_more_diagnostics。"
                ),
            }
        )
        for item in group_items:
            response_rows.append(
                {
                    "target_slot_id": item.get("target_slot_id"),
                    "context_group": context_group,
                    "review_group_id": group_id,
                    "candidate_status": status,
                    "allowed_owner_decision_codes": OWNER_DECISION_CODES,
                    "owner_decision_code": "PENDING_OWNER_OR_AUTHORIZED_DELEGATE_REVIEW",
                    "selected_candidate_rank": "PENDING_OWNER_OR_AUTHORIZED_DELEGATE_REVIEW",
                    "selected_candidate_record_ref_hash": "PENDING_OWNER_OR_AUTHORIZED_DELEGATE_REVIEW",
                    "selected_processed_value_fingerprint": "PENDING_OWNER_OR_AUTHORIZED_DELEGATE_REVIEW",
                    "owner_or_authorized_delegate_role": "PENDING_OWNER_OR_AUTHORIZED_DELEGATE_REVIEW",
                    "authorization_timestamp": "PENDING_OWNER_OR_AUTHORIZED_DELEGATE_REVIEW",
                    "basis_note": "PENDING_OWNER_OR_AUTHORIZED_DELEGATE_REVIEW",
                    "public_commit_policy": "do_not_commit_this_private_response_template_or_business_values",
                }
            )

    grouped_review = {
        "schema_version": "kmfa.private.v014_processed_value_source_map_completion_owner_review_intake.v1",
        "classification": "private_grouped_owner_review_intake_do_not_commit",
        "record_type": "v014_processed_value_source_map_completion_owner_review_intake",
        "project_id": "KMFA",
        "version": VERSION,
        "phase_id": PHASE_ID,
        "task_id": TASK_ID,
        "generated_at": generated_at,
        "source_candidate_draft_phase_id": source_draft.get("phase_id"),
        "source_candidate_status_counts": source_draft.get("candidate_status_counts", {}),
        "source_unresolved_item_count": source_diagnostic.get("unresolved_item_count"),
        "review_group_count": len(review_groups),
        "response_template_row_count": len(response_rows),
        "owner_decision_codes": OWNER_DECISION_CODES,
        "review_groups": review_groups,
    }
    response_template = {
        "schema_version": "kmfa.private.v014_processed_value_source_map_completion_owner_review_response_template.v1",
        "classification": "private_owner_review_response_template_do_not_commit",
        "record_type": "v014_processed_value_source_map_completion_owner_review_response_template",
        "project_id": "KMFA",
        "version": VERSION,
        "phase_id": PHASE_ID,
        "task_id": TASK_ID,
        "generated_at": generated_at,
        "response_template_is_active_authorization_record": False,
        "completion_template_overwritten": False,
        "active_owner_authorized_fill_record_written": False,
        "owner_decision_codes": OWNER_DECISION_CODES,
        "response_row_count": len(response_rows),
        "response_rows": response_rows,
    }
    candidate_catalog = {
        "schema_version": "kmfa.private.v014_processed_value_source_map_completion_owner_review_candidate_catalog.v1",
        "classification": "private_candidate_catalog_do_not_commit",
        "record_type": "v014_processed_value_source_map_completion_owner_review_candidate_catalog",
        "project_id": "KMFA",
        "version": VERSION,
        "phase_id": PHASE_ID,
        "task_id": TASK_ID,
        "generated_at": generated_at,
        "candidate_catalog_record_count": len(candidate_catalog_records),
        "candidate_catalog_records": candidate_catalog_records,
    }
    lines = [
        "# KMFA v0.1.4 private owner review 分组问题清单",
        "",
        "说明：本文件包含 raw 细节、候选上下文和候选值诊断，只能保留在 `.codex_private_runtime/`，不得提交 GitHub。",
        "",
        f"- 待确认 target slot：{len(response_rows)}",
        f"- 分组问题数：{len(review_groups)}",
        f"- 可选 owner decision codes：{', '.join(OWNER_DECISION_CODES)}",
        "",
    ]
    for group in review_groups:
        lines.extend(
            [
                f"## {group['review_group_id']}",
                "",
                f"- context_group：{group['context_group']}",
                f"- candidate_status：{group['candidate_status']}",
                f"- target_slot_count：{group['target_slot_count']}",
                f"- candidate_record_count：{group['candidate_record_count']}",
                f"- candidate_unique_numeric_fingerprint_count：{group['candidate_unique_numeric_fingerprint_count']}",
                "- 需要确认：该组是否可共用某个候选排名；如果不能共用，请在 response template 逐项指定。",
                "",
            ]
        )
        for record in group["top_candidate_records"][:5]:
            lines.extend(
                [
                    f"- 候选排名 {record.get('candidate_rank')}",
                    f"  - record_ref_hash: {record.get('record_ref_hash')}",
                    f"  - numeric_value_fingerprint: {record.get('numeric_value_fingerprint')}",
                    f"  - raw_file_name: {record.get('raw_file_name')}",
                    f"  - archive_member_name: {record.get('archive_member_name')}",
                    f"  - sheet_name: {record.get('sheet_name')}",
                    f"  - cell_address/page_token: {record.get('cell_address') or (str(record.get('page_index')) + '/' + str(record.get('token_index')) if record.get('page_index') else '')}",
                    f"  - raw_value: {record.get('raw_value')}",
                    f"  - context_text: {record.get('context_text')}",
                ]
            )
        lines.append("")
    return grouped_review, response_template, candidate_catalog, "\n".join(lines)


