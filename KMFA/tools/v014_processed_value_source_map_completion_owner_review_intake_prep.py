def generate(*, generated_at: str | None = None, write_governance_event: bool = True) -> dict[str, Any]:
    timestamp = _now(generated_at)
    source_draft = _read_json(SOURCE_CANDIDATE_DRAFT_PATH)
    source_diagnostic = _read_json(SOURCE_MATCH_DIAGNOSTIC_PATH)
    grouped_review, response_template, candidate_catalog, question_list = _build_private_review_package(
        generated_at=timestamp,
        source_draft=source_draft,
        source_diagnostic=source_diagnostic,
    )
    _write_json(PRIVATE_GROUPED_REVIEW_PATH, grouped_review)
    _write_json(PRIVATE_RESPONSE_TEMPLATE_PATH, response_template)
    _write_json(PRIVATE_CANDIDATE_CATALOG_PATH, candidate_catalog)
    _write_text(PRIVATE_COMPRESSED_QUESTION_LIST_PATH, question_list)
    manifest = _write_public_artifacts(
        generated_at=timestamp,
        grouped_review=grouped_review,
        response_template=response_template,
        candidate_catalog=candidate_catalog,
        source_draft=source_draft,
    )
    if write_governance_event:
        _append_development_event(timestamp, manifest)
    return manifest


